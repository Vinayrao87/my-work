package myproj;

public class Switch_case {

	public static void main(String[] args)
	{
	int sal=100;
	switch(sal)
	{
	case 5000:
		System.out.println(sal);
		break;
		
	case 1000:
		System.out.println(sal);
		break;
		
	case 200:
		System.out.println(sal);
		break;
	default:
		System.out.println("not found");
	}
	

	}

}
